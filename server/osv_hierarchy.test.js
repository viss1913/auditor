const { describe, it } = require('node:test');
const assert = require('node:assert/strict');
const { walkOsvHierarchy } = require('./osv_hierarchy');

describe('walkOsvHierarchy', () => {
    it('карточка 76: счёт → подразделение → контрагент → договор', () => {
        const data = [
            ['Карточка счета 76'],
            ['76, Расчеты с разными дебиторами и кредиторами', 100, 0, 0, 0, 50, 0],
            ['76.01, Расчеты по страхованию', 80, 0, 0, 0, 40, 0],
            ['76.01.1, Расчеты по имущественному страхованию', 60, 0, 0, 0, 30, 0],
            ['Подразделение 1', 22331.3, 0, 100, 50, 22000, 0],
            ['Контрагент 10 611', 5000, 0, 20, 10, 4900, 0],
            ['Договор 1', 1000, 0, 5, 2, 990, 0],
            ['Договор 2', 2000, 0, 10, 4, 1980, 0],
            ['Контрагент 35 603', 3000, 0, 15, 7, 2900, 0],
            ['Договор 3', 1500, 0, 8, 3, 1480, 0],
        ];

        const { rows } = walkOsvHierarchy(data);
        assert.equal(rows.length, 3);

        const d1 = rows.find((r) => r['Договор'] === 'Договор 1');
        assert.ok(d1);
        assert.equal(
            d1['Счёт, наименование счета'],
            '76.01.1, Расчеты по имущественному страхованию'
        );
        assert.equal(d1['Подразделение'], 'Подразделение 1');
        assert.equal(d1['Контрагент'], 'Контрагент 10 611');
        assert.equal(d1['Оборот Дт'], 5);

        const d3 = rows.find((r) => r['Договор'] === 'Договор 3');
        assert.ok(d3);
        assert.equal(d3['Контрагент'], 'Контрагент 35 603');
        assert.ok(!rows.some((r) => r['Объект'] === 'Подразделение 1'));
    });

    it('договор без сумм в ячейках — строка всё равно в таблице', () => {
        const data = [
            ['76.01.1, Расчеты по имущественному страхованию'],
            ['Подразделение 1'],
            ['Контрагент10 611'],
            ['Договор 1'],
            ['Договор 2'],
        ];
        const { rows } = walkOsvHierarchy(data);
        assert.equal(rows.length, 2);
        assert.equal(rows[0]['Договор'], 'Договор 1');
        assert.equal(rows[0]['Контрагент'], 'Контрагент10 611');
        assert.equal(rows[0]['Подразделение'], 'Подразделение 1');
        assert.equal(
            rows[0]['Счёт, наименование счета'],
            '76.01.1, Расчеты по имущественному страхованию'
        );
    });
});
